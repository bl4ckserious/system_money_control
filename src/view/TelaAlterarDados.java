    private void btnContaExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContaExcluirActionPerformed
        MoneyControlDao dao;
        dao = new MoneyControlDao();
        boolean excluiu;
        boolean conectou;
        int result;
        
        conectou = dao.conectar();

        if (!pswExcluirConta.getText().equals(senha)) {
            JOptionPane.showMessageDialog(null, "Senha incorreta");
            pswExcluirConta.requestFocus();
            return;
        }

        MoneyControl user;
        user = new MoneyControl(
                txtNome.getText(),
                txtSobrenome.getText(),
                txtEmail.getText(),
                txtDataNasc.getText(),
                txtCpf.getText()
        );
        conectou = dao.conectar();

        

        if (conectou) {
                result = dao.alterar(user, id);

                if (result == 1) {

                    pswExcluirConta.requestFocus();
                    JOptionPane.showMessageDialog(null, "Alteração salva.");
                    hideExcluirConta();
                    clicavel();
                    pswExcluirConta.setText("");
                    nome = txtNome.getText();
                } else {
                    JOptionPane.showMessageDialog(null, "Email já cadastrado.");
                    txtEmail.requestFocus();
                }
            
        } else {
            JOptionPane.showMessageDialog(null, "Erro conexão.");
        }
    }//GEN-LAST:event_btnContaExcluirActionPerformed

