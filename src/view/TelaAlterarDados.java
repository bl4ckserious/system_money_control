    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        AgeCheck age;
        age = new AgeCheck();
        int curAge;

        
        if (txtNome.getText().equals("") || txtSobrenome.getText().equals("") || txtDataNasc.getText().equals("") || txtEmail.getText().equals("") || txtCpf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return;
        }
        
        SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
        try {
            java.sql.Date data = new java.sql.Date(format.parse(txtDataNasc.getText()).getTime());
            curAge = AgeCheck.calculaIdade(data);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            return;
        }

        if (curAge >= 0 && curAge < 18 ) {
            JOptionPane.showMessageDialog(null, "Apenas maiores de idade podem criar uma conta.");
            txtDataNasc.requestFocus();
            return;
        } else if (curAge > 110 || curAge < 0) {
            JOptionPane.showMessageDialog(null, "Data inválida.");
            txtDataNasc.requestFocus();
            return;
        } 
        showExcluirConta();
        naoClicavel();
        btnEsconder4.setVisible(false);
        pswExcluirConta.requestFocus();
        
    }//GEN-LAST:event_btnAlterarActionPerformed

