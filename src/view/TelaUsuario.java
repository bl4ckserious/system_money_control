    private void btnEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntradaActionPerformed
        boolean resposta;
        double result;
        MoneyControlDao dao;
        dao = new MoneyControlDao();
        
        if (txtValorEntrada.getText().equals("") || txtDataEntrada.getText().equals("  /  /    ")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
            txtValorEntrada.requestFocus();
            return;
        }
        
        String newValue;
        newValue = txtValorEntrada.getText().replaceAll(",", ".");
        double cV = Double.parseDouble(newValue);
        
        
        
        MoneyControl entrada;
        entrada = new MoneyControl(
                cV,
                cmbCategoriaEntrada.getSelectedItem().toString(),
                txtDataEntrada.getText()
        );
        
        resposta = dao.conectar();
        if (resposta) {
            result = dao.addEntrada(entrada, id);
            if (result == 1) {
                limparEntrada();
                txtValorEntrada.requestFocus();

                txtSaldo.setText(" R$ " + saldoAqui(id));
            } else {
                JOptionPane.showMessageDialog(null, "Erro de conexão. Tente novamente");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Erro de conexão.");
        }
    }//GEN-LAST:event_btnEntradaActionPerformed

