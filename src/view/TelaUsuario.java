    private String saldoAqui(int id) {
        DecimalFormat df = new DecimalFormat("#,###.00");
        
        boolean resposta;
        MoneyControlDao dao = new MoneyControlDao();
        resposta = dao.conectar();
        double saldo = dao.saldo(id);

        if (saldo < 0) {
            txtSaldo.setForeground(MY_RED);
            return df.format(saldo);
        } else if (saldo > 0) {
            txtSaldo.setForeground(HARD_GREEN);
            return df.format(saldo);
        } else if (saldo == 0) {
            return "0,00";
        }
        return df.format(saldo);
    }

